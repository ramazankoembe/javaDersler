package odev1;

public class ValueDegerleri {

	public static void main(String[] args) {

		System.out.println("Odev 2 Primitive Date Typs Value");

		byte a = 32;
		System.out.println("Byte Data Typ/ Alt ve Üst Degerleri");
		System.out.println(a);
		System.out.println(Byte.MIN_VALUE);
		System.out.println(Byte.MAX_VALUE);

		short b = 1990;
		System.out.println("Short Data Typ/ Alt ve Üst Degerleri");
		System.out.println(b);
		System.out.println(Short.MIN_VALUE);
		System.out.println(Short.MAX_VALUE);

		int c = 21863590;
		System.out.println("Int Data Typ/ Alt ve Üst Degerleri");
		System.out.println(c);
		System.out.println(Integer.MIN_VALUE);
		System.out.println(Integer.MAX_VALUE);

		long d = 3426598745L;
		System.out.println("Long Data Typ/ Alt ve Üst Degerleri");
		System.out.println(d);
		System.out.println(Long.MIN_VALUE);
		System.out.println(Long.MAX_VALUE);

		float e = 0.1234567890123F;
		System.out.println("Float Data Typ/ Alt ve Üst Degerleri");
		System.out.println(e);
		System.out.println(Float.MIN_VALUE);
		System.out.println(Float.MAX_VALUE);

		double f = 2.3;
		System.out.println("Double Data Typ/ Alt ve Üst Degerleri");
		System.out.println(f);
		System.out.println(Double.MIN_VALUE);
		System.out.println(Double.MAX_VALUE);
	}

}
